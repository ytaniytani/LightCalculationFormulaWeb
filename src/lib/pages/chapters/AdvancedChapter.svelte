  <h3>スペキュラ IBL(鏡面側の間接反射)</h3>
  <p>
    第10章の SH は、拡散(ざらざら)側の間接光でした。では<strong>鏡面(つるつる)側</strong>の
    間接反射——金属に映り込む周囲の景色——はどうするか。これには
    <strong>プリフィルタ環境マップ</strong>という、周囲の景色をラフネスごとにぼかして用意しておく
    テクニックを使います。SH と対になる「鏡面版の周囲光」だと思ってください。
  </p>

  <h3>トーンマッピングと HDR(なぜ白飛びしないのか)</h3>
  <p>
    現実の光は、太陽のようにとても明るいものから暗い影まで、桁違いの幅があります。これを
    <strong>HDR(ハイダイナミックレンジ)</strong>でそのまま計算し、最後に画面に収まる 0〜1 へ
    上手に圧縮するのが<strong>トーンマッピング</strong>。これがないと明るい部分がすぐ真っ白に
    飛んでしまいます。写真の露出調整に近い話です。
  </p>

  <h3>影(シャドウ)の基礎</h3>
  <p>
    ここまでのデモには、実は<strong>落ち影(ある物が別の物にかける影)</strong>がありません。
    面が自分でライトに背を向ける「陰(shade)」は内積で出せますが、他の物にさえぎられる
    「影(shadow)」は別のしくみ——<strong>シャドウマップ</strong>——が必要です。
    「ライトから見て、手前にあるかどうか」を事前に描いて判定します。
  </p>

  <h3>異方性ハイライト</h3>
  <p>
    ヘアラインの入った金属や髪の毛は、ハイライトが<strong>一方向に伸びます</strong>。これは
    表面の微小な溝に向きがあるため。ラフネスを縦横で別々に持たせると表現できます。
  </p>

